    public static hex2binstr(hexNum: string, strLen: number) {
        const replaceChar = "O";
        const binNum = parseInt(hexNum, 16).toString(2).replace(/0/ig, replaceChar); 
        let binstr = binNum;
    
        for (let i = 0; i < strLen - binNum.length ; i++ ) {
            binstr = replaceChar + binstr;
        }
    
        return binstr;
    }
    
